def main():
    daysNum = int(input("Enter a number (1-7) for the day of the week: "))
    
    if daysNum == 1:
        print("Monday")

    elif daysNum == 2:
        print("Tuesday")
    
    elif daysNum == 3:
        print("Wednesday")
    
    elif daysNum == 4:
        print("Thursday")
        
    elif daysNum == 5:
        print("Friday")
    
    elif daysNum == 6:
        print("Saturday")
        
    elif daysNum == 7:
        print("Sunday")
    
    else:
        print("Error: Please enter a number in the range 1-7.")

main()

